#include "ft_printf.h"

int	ft_putstr(char *str)
{
	int	i;
	int	test;

	i = 0;
	while (str[i])
	{
		test = ft_putchar(str[i]);
		if (test == -1)
			return (-1);
		i++;
	}
	return (1);
}
