void	ft_putnbr(int n)
{
	int	tmp;

	if (n == -2147483648)
	{
		write(1, "-2147483648", 11);
		return ;
	}
	if (n < 0)
	{
		write(1, "-", 1);
		ft_putnbr_fd(-n, 1);
	}
	if (n >= 0)
	{
		tmp = n % 10 + '0';
		if (n > 9)
			ft_putnbr_fd(n / 10, 1);
		write(1, &tmp, 1);
	}
}